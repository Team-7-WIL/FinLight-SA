// Merge missing translation keys from English into every locale
// to avoid {missing translation} placeholders.
const deepMerge = (target, fallback) => {
  const result = Array.isArray(fallback) ? [] : { ...target };
  Object.keys(fallback).forEach((key) => {
    const fbVal = fallback[key];
    const tgtVal = target?.[key];
    if (fbVal && typeof fbVal === 'object' && !Array.isArray(fbVal)) {
      result[key] = deepMerge(tgtVal || {}, fbVal);
    } else if (tgtVal === undefined || tgtVal === null) {
      result[key] = fbVal;
    } else {
      result[key] = tgtVal;
    }
  });
  return result;
};

